#printing multiple times without repeating the code:
#for a junior engineer
number_of_time = 3
while number_of_time != 0 :
    print("Hello World!")
    number_of_time = number_of_time - 1
    
#for a middle engineer
number_of_time = 1
while number_of_time <= 3 :
    print("Hello World!")
    number_of_time = number_of_time + 1

#for senior engineer
number_of_time = 0
while number_of_time < 3 :
    print("Hello World!")
    number_of_time += 1
    
