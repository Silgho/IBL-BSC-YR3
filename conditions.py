# Using the if conditional expression.
x = int(input("Enter the value of X : "))
y = int(input("Enter the value of Y : "))

if x < y:
    print("x is less than y.")
    
if x > y:
    print("x is greater than y.")

if x == y :
    print("X is equal to Y.") 
